public class Characters {
    public static void main(String[] args) {
        char[] characters = {
            'A', 'B', 'C', 'D', 'E',
            'F', 'G', 'H', 'I', 'J'
        };

        System.out.println("Characters:");
        for (char ch : characters) {
            System.out.println(ch);
        }
    }
}